// Вспомогательная функция для вывода матрицы
void print_matrix(char arr[9][9], int n) {
    printf("Matrix %dx%d:\n", n, n);
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            printf("%c ", arr[i][j]);
        }
        printf("\n");
    }
    printf("\n");
}

